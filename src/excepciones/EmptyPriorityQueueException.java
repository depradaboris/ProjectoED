package excepciones;

public class EmptyPriorityQueueException extends Exception {

	public EmptyPriorityQueueException() {
		super();
	}

	public EmptyPriorityQueueException(String arg0) {
		super(arg0);
	}

	public EmptyPriorityQueueException(Throwable arg0) {
		super(arg0);
	}

	public EmptyPriorityQueueException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public EmptyPriorityQueueException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
